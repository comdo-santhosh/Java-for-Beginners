

// relational operator  -->   > , < , >= , <= , !=
public class Relational_operator {
    public static void main(String[] args) {

        int n1 = 76;
        int n2 = 85;

        System.out.println(n1 > n2);  // false
        System.out.println(n1 < n2); // true
        System.out.println(n1 <= n2); // true
        System.out.println(n1 >= n2); // false
        System.out.println(n1 != n2); // true
        System.out.println("");
        int num1 = 25;
        int num2 = 25;
        System.out.println(num1 < num2);
        System.out.println(num1 > num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 != num2);

    }
}
